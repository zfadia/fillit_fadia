int		ft_iterative_factorial(int nb)
{
	int r;
	int i;

	r = 1;
	i = 1;
	if (nb > 12 || nb < 0)
		return (0);
	while (i <= nb)
	{
		r = (i * r);
		i++;
	}
	return (r);
}
